package name.martingeisse.reporting.document;

/**
 * This enum specifies formatting instructions that can be applied to inline content.
 */
public enum InlineFormattingInstruction {

	/**
	 * the NONE
	 */
	NONE,
	
	/**
	 * the BOLD
	 */
	BOLD,
	
	/**
	 * the ITALIC
	 */
	ITALIC,
	
	/**
	 * the UNDERLINE
	 */
	UNDERLINE,
	
	/**
	 * the STRIKE_THROUGH
	 */
	STRIKE_THROUGH;
	
}
